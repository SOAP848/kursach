#!/bin/bash
# =============================================================================
# docker-entrypoint.sh — точка входа для Docker-контейнера
# =============================================================================
# Позволяет запускать различные режимы анализа через аргументы:
#   test    — запуск pytest (по умолчанию)
#   bandit  — запуск bandit SAST-анализа
#   lint    — запуск flake8 линтинга
#   all     — запуск всех проверок последовательно
# =============================================================================

set -e

MODE="${1:-test}"

case "$MODE" in
    test)
        echo "=========================================="
        echo " Запуск тестов (pytest)..."
        echo "=========================================="
        exec pytest tests/ -v --tb=short
        ;;
    bandit)
        echo "=========================================="
        echo " Запуск SAST-анализа (bandit)..."
        echo "=========================================="
        exec bandit -r . -c .bandit
        ;;
    lint)
        echo "=========================================="
        echo " Запуск линтинга (flake8)..."
        echo "=========================================="
        exec flake8 . --count --select=E9,F63,F7,F82 --show-source --statistics --ignore=F722,F821
        ;;
    all)
        echo "=========================================="
        echo " Шаг 1/3: Запуск тестов (pytest)..."
        echo "=========================================="
        pytest tests/ -v --tb=short
        echo ""
        echo "=========================================="
        echo " Шаг 2/3: Запуск SAST-анализа (bandit)..."
        echo "=========================================="
        bandit -r . -c .bandit
        echo ""
        echo "=========================================="
        echo " Шаг 3/3: Запуск линтинга (flake8)..."
        echo "=========================================="
        flake8 . --count --select=E9,F63,F7,F82 --show-source --statistics --ignore=F722,F821
        echo ""
        echo "=========================================="
        echo " Все проверки завершены."
        echo "=========================================="
        ;;
    *)
        echo "Неизвестный режим: $MODE"
        echo "Допустимые режимы: test, bandit, lint, all"
        exit 1
        ;;
esac
